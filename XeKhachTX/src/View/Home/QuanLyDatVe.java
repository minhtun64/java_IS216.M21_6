    public void fillTable() {
        try {
            Class.forName("oracle.jdbc.OracleDriver");
            conn = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl", "DB_XEKHACH", "userpass");
            java.sql.Statement st = conn.createStatement();
            String sql = "SELECT * FROM DATVE";
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                String iddv = rs.getString("ID_DATVE");
                String idhk = rs.getString("ID_HANHKHACH");
                String idv = rs.getString("ID_VE");
                String idnv = rs.getString("ID_NHANVIEN");
                String ngaythem = String.valueOf(new SimpleDateFormat("dd-MM-yyyy HH:mm:ss").format(rs.getTimestamp("NGAYTHEM")));
                String tongtien = rs.getString("TONGTIEN");
                String tt = rs.getString("TINHTRANG");
                String tbData[] = {iddv, idhk, idv, idnv, ngaythem, tongtien, tt};
                DefaultTableModel tblModel = (DefaultTableModel) danhsachdatve.getModel();

                tblModel.addRow(tbData);
            }

        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null, e);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

