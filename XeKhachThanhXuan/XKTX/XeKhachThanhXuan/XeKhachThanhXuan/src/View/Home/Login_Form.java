    private void jButton_dangnhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_dangnhapActionPerformed
        // TODO add your handling code here:
        String username = textTenDN.getText();
        String password = textMK.getText();
        DangNhap ur = new DangNhap();
        if (username.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên đăng nhập\n");
        } else if (password.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mật khẩu\n");
        } else {
            try {
                if (ur.DangNhap(username, password) == 1) {
                    if (ur.DangNhap_PhanQuyen(username, password) == 1) {
                        Homepage_NguoiNhanVien homepagenhanvien = new Homepage_NguoiNhanVien();
                        homepagenhanvien.setVisible(true);
                        this.setVisible(false);
                    } else {
                        Homepage_NguoiQuanLy homepagequanly = new Homepage_NguoiQuanLy();
                        homepagequanly.setVisible(true);
                        this.setVisible(false);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Đăng nhập thất bại");
                }
            }  catch (Exception e) {
            System.out.println(e);
        }
        }
    }//GEN-LAST:event_jButton_dangnhapActionPerformed

