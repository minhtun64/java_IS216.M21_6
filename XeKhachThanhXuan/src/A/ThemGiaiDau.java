    private void themGiaiDauActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themGiaiDauActionPerformed
        int countResult = 0;
        // TODO add your handling code here:
        String maGiaiText = maGiai.getText();
        String tenGiaiText = tenGiai.getText();
        // Bat try catch ngay dinh dang sai format
        SimpleDateFormat DateFormat = new SimpleDateFormat("dd/MM/yyyy");

        try {
            Date ngayBDDate = DateFormat.parse(ngayBD.getText());
            Date ngayKTDate = DateFormat.parse(ngayKT.getText());
            GiaiDau gd = new GiaiDau();
            countResult = gd.themGiaiDau(maGiaiText, tenGiaiText, ngayBDDate, ngayKTDate);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Có lỗi trong quá trình insert",
                    "Ngày sai định dạng", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(ThemGiaiDau.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(countResult == 0) {
            JOptionPane.showMessageDialog(this, "Insert không thành công!",
                    "Lỗi insert CSDL", JOptionPane.ERROR_MESSAGE);
        } else if(countResult == 1) {
              JOptionPane.showMessageDialog(this, "Thêm thành công!");
        }
    }//GEN-LAST:event_themGiaiDauActionPerformed

