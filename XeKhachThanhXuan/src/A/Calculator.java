    private void tinh()
    {
        if(this.phepToan.equals("")) this.kq=Long.parseLong(IbHienThi.getText());
        else
        {
            if(this.phepToan.equals("+")) this.kq+=Long.parseLong(IbHienThi.getText());
            if(this.phepToan.equals("-")) this.kq-=Long.parseLong(IbHienThi.getText());
            if(this.phepToan.equals("X")) this.kq*=Long.parseLong(IbHienThi.getText());
            if(this.phepToan.equals("/")) this.kq/=Long.parseLong(IbHienThi.getText());
        }
    }
    
