    private void btSigninActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSigninActionPerformed
        // TODO add your handling code here:
        String username = usern.getText();
        String password = new String (pass.getPassword());
        
        if (username.equals("") || password.equals("")) {
            JOptionPane.showMessageDialog(this, "Username hay Password trống", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        tableuser ac = new tableuser();
        boolean result = false;
        result = ac.Login(username, password);
        if (result) {
            JOptionPane.showMessageDialog(this, "Đăng nhập thành công");       
        } else {
            JOptionPane.showMessageDialog(this, "Không có thông tin", "Failure", JOptionPane.ERROR_MESSAGE);
        }
        return;
    }//GEN-LAST:event_btSigninActionPerformed

    private void btsignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsignupActionPerformed
        // TODO add your handling code here:
        
        String username = usern.getText();
        if (username.equals("") ) {
            JOptionPane.showMessageDialog(this, "Username không được trống", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String password = new String (pass.getPassword());
        if (password.equals("") ) {
            JOptionPane.showMessageDialog(this, "Password không được trống!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String confirm = new String (conf.getPassword());
        if (!password.equals(confirm) ) {
            JOptionPane.showMessageDialog(this, "Password và confirm password phải giống nhau", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        tableuser ac = new tableuser();
        String checkUsername = ac.getUsername(username);
        if (username.equals(checkUsername)) {
            JOptionPane.showMessageDialog(this, "Username đã có!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int result = 0;
        try {
            result = ac.themtaikhoan(username, password);
            System.out.println(result);
        } catch (SQLException ex) {
            Logger.getLogger(Sign_in_up.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Đăng ký thành công!");
            hide();
        } else {
            JOptionPane.showMessageDialog(this, "Đăng ký thất bại", "Failure", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btsignupActionPerformed

