    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        int idve = Integer.valueOf((String) IDVe.getSelectedItem());
        int idnhanvien = Integer.valueOf((String) IDNhanVien.getSelectedItem());
        String tenhanhkhach = tenHanhKhach.getText();
        String sodt = soDT.getText();
        String cmnd = cMND.getText();
        String email = eMail.getText();
        boolean flag = true;
        if (tenhanhkhach.equals("")) {

            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên hành khách\n");
            flag = false;
        } else if (sodt.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số điện thoại\n");
            flag = false;
        } else if (cmnd.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập CMND\n");
            flag = false;
        } else if (email.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Email");
            flag = false;
        }

        if (flag == true) {
            String reg = "^(0|\\+84)(\\s|\\.)?((3[2-9])|(5[689])|(7[06-9])|(8[1-689])|(9[0-46-9]))(\\d)(\\s|\\.)?(\\d{3})(\\s|\\.)?(\\d{3})$";
            boolean flag2 = sodt.matches(reg);
            if (flag2 == false) {
                JOptionPane.showMessageDialog(this, "Số điện thoại không đúng");
                flag = false;
            }
        }

        if (flag == true) {
            {
                String cmndPattern = "\\d{9}";
                boolean flag3 = cmnd.matches(cmndPattern);
                if (flag3 == false) {
                    JOptionPane.showMessageDialog(this, "CMND không hợp lệ");
                    flag = false;
                }
            }
            if (flag == true) {
                String emailPattern = "\\w+@\\w+[.]\\w+([.]\\w+)?";
                boolean flag4 = email.matches(emailPattern);
                if (flag4 == false) {
                    JOptionPane.showMessageDialog(this, "Email không hợp lệ");
                    flag = false;
//                }
                }
            }
            if (flag == true) {
                Ve add_dat_ve = new Ve();
                int z = 0;
                try {
                    z = add_dat_ve.ThemDatVe(idve, idnhanvien, tenhanhkhach, sodt, cmnd, email);
                } catch (SQLException ex) {
                    Logger.getLogger(Them_DatVe.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(Them_DatVe.class.getName()).log(Level.SEVERE, null, ex);
                }
                if (z > 0) {
                    JOptionPane.showMessageDialog(this, "thêm thành công!", "Thông báo",
                            JOptionPane.INFORMATION_MESSAGE, null);
                    DefaultTableModel model = (DefaultTableModel) danhsachdatve.getModel();
                    model.setRowCount(0);
                    fillTable();
                } else {
                    JOptionPane.showMessageDialog(null, "Error");
                }
            }
        }

    }//GEN-LAST:event_AddActionPerformed

