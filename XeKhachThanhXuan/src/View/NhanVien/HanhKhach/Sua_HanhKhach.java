    private void nutsuathem_quanlytuyenxeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nutsuathem_quanlytuyenxeActionPerformed
        DefaultTableModel suahanhkhach = (DefaultTableModel) danhsachhanhkhach.getModel();
        int i = danhsachhanhkhach.getSelectedRow();

        if (danhsachhanhkhach.getSelectedRowCount() == 1) {
            String x = ID_HanhKhach.getText();
            int idhanhkhach = Integer.parseInt(x);
            String tenhanhkhach = tenHanhKhach.getText();
            String sodt = soDT.getText();
            String cmnd = cMND.getText();
            String email = eMail.getText();
//            String tinhtrang = tinhTrang.getText();
            boolean flag = true;
            if (tenhanhkhach.equals("")) {

                JOptionPane.showMessageDialog(this, "Vui lòng nhập tên hành khách\n");
                flag = false;
            } else if (sodt.equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập số điện thoại\n");
                flag = false;
            } else if (cmnd.equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập CMND\n");
                flag = false;
            } else if (email.equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập Email");
                flag = false;
            }
            if (flag == true) {
                String reg = "^(0|\\+84)(\\s|\\.)?((3[2-9])|(5[689])|(7[06-9])|(8[1-689])|(9[0-46-9]))(\\d)(\\s|\\.)?(\\d{3})(\\s|\\.)?(\\d{3})$";
                boolean flag2 = sodt.matches(reg);
                if (flag2 == false) {
                    JOptionPane.showMessageDialog(this, "Số điện thoại không đúng hoặc không hợp lệ");
                    flag = false;
                }
            }

            if (flag == true) {
                {
                    String cmndPattern = "\\d{9}";
                    boolean flag3 = cmnd.matches(cmndPattern);
                    if (flag3 == false) {
                        JOptionPane.showMessageDialog(this, "CMND không hợp lệ");
                        flag = false;
                    }
                }
            }
            if (flag == true) {
                String emailPattern = "\\w+@\\w+[.]\\w+([.]\\w+)?";
                boolean flag4 = email.matches(emailPattern);
                if (flag4 == false) {
                    JOptionPane.showMessageDialog(this, "Email không hợp lệ");
                    flag = false;
                }
            }

            if (flag == true) {
                HanhKhach sua_hanhkhach = new HanhKhach();
                int z = 0;
                z = sua_hanhkhach.SuaHanhKhach(idhanhkhach, tenhanhkhach, sodt, cmnd, email);
                if (z > 0) {
                    JOptionPane.showMessageDialog(this, "Sửa thành công!", "Thông báo",
                            JOptionPane.INFORMATION_MESSAGE, null);
                    QuanLyHanhKhach quanlyhanhkhach = new QuanLyHanhKhach();
                    quanlyhanhkhach.setVisible(true);
                    CloseFrame();
                } else {
                    JOptionPane.showMessageDialog(null, "Error");
                }
            }
        } else {
            if (danhsachhanhkhach.getSelectedRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Mời chọn 1 tuyến xe cần sửa!",
                        "Lỗi thao tác", JOptionPane.WARNING_MESSAGE, null);
            } else {
                JOptionPane.showMessageDialog(this, "Vui lòng chỉ chọn 1 tuyến xe cần sửa!",
                        "Lỗi thao tác", JOptionPane.WARNING_MESSAGE, null);
            }
        }

    }//GEN-LAST:event_nutsuathem_quanlytuyenxeActionPerformed

