    public void fillTable() {
        try {
            Class.forName("oracle.jdbc.OracleDriver");
            conn = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl", "NHANVIEN", "nhanvien");
            java.sql.Statement st = conn.createStatement();
            String sql = "SELECT ID_HANHKHACH, TENHK, CMND, SDT, EMAIL, TINHTRANG FROM HANHKHACH";
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                String idhanhkhachh = rs.getString("ID_HANHKHACH");
                String tenhanhkhach = rs.getString("TENHK");
                String sodt = rs.getString("SDT");
                String cmnd = rs.getString("CMND");
                String email = rs.getString("EMAIL");
                String tinhtrang = rs.getString("TINHTRANG");
                String tbData[] = {idhanhkhachh, tenhanhkhach, sodt, cmnd, email, tinhtrang};
                DefaultTableModel tblModel = (DefaultTableModel) danhsachhanhkhach.getModel();

                tblModel.addRow(tbData);
            }
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null, e);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

