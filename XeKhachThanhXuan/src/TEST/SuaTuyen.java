    private void nutsua_quanlytuyenxethem_quanlytuyenxeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nutsua_quanlytuyenxethem_quanlytuyenxeActionPerformed
        DefaultTableModel suatuyenxe = (DefaultTableModel) danhsachtuyenxe.getModel();
        int i = danhsachtuyenxe.getSelectedRow();

        if (danhsachtuyenxe.getSelectedRowCount() == 1) {
            String iD = matuyen.getText();
            String tenTuyen = tentuyen.getText();
            String diemDau = diemdau.getText();
            String diemCuoi = diemcuoi.getText();
            String soKm = sokm.getText();
            String soGioDi = sogiodi.getText();

            suatuyenxe.setValueAt(matuyen.getText(), i, 0);
            suatuyenxe.setValueAt(tentuyen.getText(), i, 1);
            suatuyenxe.setValueAt(diemdau.getText(), i, 2);
            suatuyenxe.setValueAt(diemcuoi.getText(), i, 3);
            suatuyenxe.setValueAt(sokm.getText(), i, 4);
            suatuyenxe.setValueAt(sogiodi.getText(), i, 5);

            TuyenXe biensuatuyenxe = new TuyenXe();

            //Lay ket qua tu CSDL
            int sodongsuatuyenxe = biensuatuyenxe.suaTuyenXe(iD, tenTuyen, diemDau,
                    diemCuoi, soKm, soGioDi);

            if (sodongsuatuyenxe > 0) {
                JOptionPane.showMessageDialog(this, "Sửa thành công!","Thông báo",
                    JOptionPane.INFORMATION_MESSAGE,null);
                hide();

                SuaTuyen classsuatuyenxe = new SuaTuyen();
                classsuatuyenxe.setVisible(true);
                this.setVisible(false);

            } else {
                JOptionPane.showMessageDialog(null, "Error");
            }
        } else {
            if (danhsachtuyenxe.getSelectedRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Mời chọn 1 tuyến xe cần sửa!",
                        "Lỗi thao tác", JOptionPane.WARNING_MESSAGE, null);
                SuaTuyen classsuatuyenxe = new SuaTuyen();
                classsuatuyenxe.setVisible(true);
                this.setVisible(false);

            } else {
                JOptionPane.showMessageDialog(this, "Vui lòng chỉ chọn 1 tuyến xe cần sửa!",
                        "Lỗi thao tác", JOptionPane.WARNING_MESSAGE, null);
                SuaTuyen classsuatuyenxe = new SuaTuyen();
                classsuatuyenxe.setVisible(true);
                this.setVisible(false);

            }
        }
    }//GEN-LAST:event_nutsua_quanlytuyenxethem_quanlytuyenxeActionPerformed

