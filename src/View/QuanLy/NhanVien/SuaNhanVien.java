    public void fillTable() {
        try {
            Class.forName("oracle.jdbc.OracleDriver");
            con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl", "c##TEST3", "Square1");
            Statement st = con.createStatement();
            String sql = "select * from NHANVIEN WHERE tinhtrang = 'Hoạt động' ";
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {

                String maNhanVien = rs.getString("ID_NHANVIEN");
                String maNQL = rs.getString("ID_NQL");
                String tenDangNhap = rs.getString("TENDANGNHAP");
                String tenNhanVien = rs.getString("TENNV");
                String gioiTinh = rs.getString("GIOITINH");
                String ngaySinh = String.valueOf(new SimpleDateFormat("dd-MM-yyyy").format(rs.getTimestamp("NGAYSINH")));
                String ngayVaoLam = String.valueOf(new SimpleDateFormat("dd-MM-yyyy").format(rs.getTimestamp("NGAYVAOLAM")));
                String sDT = rs.getString("SDT");
                String eMail = rs.getString("EMAIL");
                String tbData[] = {maNhanVien, maNQL, tenDangNhap, tenNhanVien, gioiTinh, ngaySinh, ngayVaoLam, sDT, eMail};
                DefaultTableModel tblModel = (DefaultTableModel) danhsachnhanvien_nhanvien.getModel();

                tblModel.addRow(tbData);
            }

        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null, e);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

