    public void fillTable() {
        try {
            Class.forName("oracle.jdbc.OracleDriver");
            con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl", "c##TEST3", "Square1");
            Statement st = con.createStatement();
            String sql = "select * from LOAIXE  WHERE tinhtrang = 'Hoạt động' ";
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                String id = String.valueOf(rs.getInt("ID_LOAIXE"));
                String tenloaixe = rs.getString("TENLOAIXE");
                String soghe = rs.getString("SOLUONGGHE");
                String tbData[] = {id, tenloaixe, soghe};
                DefaultTableModel tblModel = (DefaultTableModel) dsloaixe_loaixe.getModel();

                tblModel.addRow(tbData);
            }

        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null, e);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }


